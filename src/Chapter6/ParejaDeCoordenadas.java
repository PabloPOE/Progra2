package Chapter6;

/**
 *
 * @author Usuario
 */
public class ParejaDeCoordenadas {
     String x = "";
     double y = 0.0;

    public ParejaDeCoordenadas(String x, double y)
    {
        this.x = x;
        this.y = y;
    }

    public ParejaDeCoordenadas()
    {

    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }


}
    

